namespace Win11Optimization.Core.Models;

/// <summary>
/// Уровень риска оптимизации.
/// Используется для предупреждения пользователя перед применением.
/// </summary>
public enum RiskLevel
{
    /// <summary>Нет риска — косметические или информационные изменения.</summary>
    None = 0,

    /// <summary>Низкий риск — легко обратимые изменения параметров.</summary>
    Low = 1,

    /// <summary>Средний риск — изменения реестра, влияющие на поведение системы.</summary>
    Medium = 2,

    /// <summary>Высокий риск — отключение служб, изменение критических параметров.</summary>
    High = 3,

    /// <summary>Критический риск — потенциально может нарушить работу системы.</summary>
    Critical = 4
}
